Error __MOV(CPU* cpu, Memory* mem, const Instruction* inst)
{
    Word dataToCopy = -1;
    if (inst->registerMode & o2_imm) {

        dataToCopy = inst->operand2;
        printf("\nSRC Data : %d \tMode : Immediate\n", dataToCopy);

    } else if (inst->registerMode & o2_mem) {

        dataToCopy = readFromMemory(mem, inst->operand2);
        printf("\nSRC Data : %d \tMode : Memory address [%d]\n", dataToCopy, inst->operand2);

    } else if (inst->registerMode & o2_reg) {

        switch (inst->operand2) {
        case 1:
            dataToCopy = cpu->registers.AX.full;
            printf("\nSRC Data : %d \tMode : Register AX\n", dataToCopy);
            break;
        case 2:
            dataToCopy = cpu->registers.BX.full;
            printf("\nSRC Data : %d \tMode : Register BX\n", dataToCopy);
            break;
        case 3:
            dataToCopy = cpu->registers.CX.full;
            printf("\nSRC Data : %d \tMode : Register CX\n", dataToCopy);
            break;
        case 4:
            dataToCopy = cpu->registers.DX.full;
            printf("\nSRC Data : %d \tMode : Register DX\n", dataToCopy);
            break;
        }
    }

    if (inst->registerMode & o1_mem) {

        writeToMemory(mem, inst->operand, dataToCopy);
        printf("DST Data : %d \tMode : Memory address [%d]\n", readFromMemory(mem, inst->operand), inst->operand);

    } else if (inst->registerMode & o1_reg) {

        switch (inst->operand) {

        case 1:
            cpu->registers.AX.full = dataToCopy;
            printf("DST Data : %d \tMode : Register AX\n", cpu->registers.AX.full);
            break;
        case 2:
            cpu->registers.BX.full = dataToCopy;
            printf("DST Data : %d \tMode : Register BX\n", cpu->registers.BX.full);
            break;
        case 3:
            cpu->registers.CX.full = dataToCopy;
            printf("DST Data : %d \tMode : Register CX\n", cpu->registers.CX.full);
            break;
        case 4:
            cpu->registers.DX.full = dataToCopy;
            printf("DST Data : %d \tMode : Register DX\n", cpu->registers.DX.full);
            break;
        }
    }

    return ERR_OK;
}
